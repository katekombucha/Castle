using CodeBase.GameEntities;
using UnityEditor;
using UnityEngine;

namespace Editor
{
    [CustomEditor(typeof(EntityMarker))]
    public class EntityMarkerEditor : UnityEditor.Editor
    {
        [DrawGizmo(GizmoType.Active | GizmoType.Pickable | GizmoType.NonSelected)]
        public static void RenderCustomGizmo(EntityMarker spawner, GizmoType gizmo)
        {
            Gizmos.color = Color.red;
            Gizmos.DrawSphere(spawner.transform.position, 1f);
        }
    }
}